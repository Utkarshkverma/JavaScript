// Data Type = Premitive and Refrences

/**
 * Premitive = created in stack memory
 * 
 * number  = number 
 * String = any string
 * boolean = true/false
 * null = null
 * undefined = undefined
 * symbol
 */

/**
 * Refrence =  created in heap memory
 * Array
 * Object
 * Date
 * Function
 * Set
 * Map
 */

let a = 5;
console.log(typeof a);

let b = "Hlw"
console.log(typeof b);

let c = false
console.log(typeof c);


let d = null
console.log(typeof d);

let e;
console.log(typeof e);

// Ref type

let names = ['a','b','c','d']
console.log(typeof names);


